console.log("****** Before Swap ******");

var sweety = "Sweety";
var cutie = "Cutie";
console.log(sweety);
console.log(cutie); 

console.log("****** After Swap ******");

var temp = "Sweety";
sweety = cutie;
cutie = temp;
console.log(sweety);
console.log(cutie);

console.log("****** Before Swapping Number ******");

var num1 = 100;
var num2 = 200;
var num3 = 300;
console.log(num1, num2, num3);

console.log("****** After Swapping Number ******");
var temp = num1;
num1 = num2;
num2 = num3;
num3 = temp;
console.log(num1, num2, num3);